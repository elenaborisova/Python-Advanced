command = input()
numbers = list(map(int, input().split()))

if command == "Odd":
    odd_nums_sum = sum(filter(lambda num: num % 2 == 1, numbers))
    print(odd_nums_sum * len(numbers))
elif command == "Even":
    even_nums_sum = sum(filter(lambda num: num % 2 == 0, numbers))
    print(even_nums_sum * len(numbers))
